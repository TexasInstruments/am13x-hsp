function loadAndRun(hCS, executableFile, varargin)
% LOADANDRUN Deploy an application binary to the AM13E2X target via CCS scripting.
%
%   loadAndRun(hCS, executableFile)
%   loadAndRun(hCS, executableFile, ...)
%
%   Parameters:
%       hCS            - Active Simulink configuration set handle (currently
%                        unused; reserved for future target-specific config
%                        retrieval such as hardware board name or core arch).
%                        The active config set is re-fetched internally via
%                        getActiveConfigSet(bdroot).
%       executableFile - Full path to the compiled application binary (.out)
%                        generated by Simulink code generation.
%                        E.g. 'C:/work/model_ert_rtw/model.out'
%       varargin       - Reserved for future use. Currently unused.
%
%   Migration help from older DSS to new CCS scripting API:
%   https://downloads.ti.com/ccs/esd/documents/users_guide_ccs_20.4.0/ccs_debug-scripting-migration.html

%  Copyright (C) 2026 Texas Instruments Incorporated
%
%  Redistribution and use in source and binary forms, with or without
%  modification, are permitted provided that the following conditions
%  are met:
%
%    Redistributions of source code must retain the above copyright
%    notice, this list of conditions and the following disclaimer.
%
%    Redistributions in binary form must reproduce the above copyright
%    notice, this list of conditions and the following disclaimer in the
%    documentation and/or other materials provided with the
%    distribution.
%
%    Neither the name of Texas Instruments Incorporated nor the names of
%    its contributors may be used to endorse or promote products derived
%    from this software without specific prior written permission.
%
%  THIS SOFTWARE IS PROVIDED BY THE COPYRIGHT HOLDERS AND CONTRIBUTORS
%  "AS IS" AND ANY EXPRESS OR IMPLIED WARRANTIES, INCLUDING, BUT NOT
%  LIMITED TO, THE IMPLIED WARRANTIES OF MERCHANTABILITY AND FITNESS FOR
%  A PARTICULAR PURPOSE ARE DISCLAIMED. IN NO EVENT SHALL THE COPYRIGHT
%  OWNER OR CONTRIBUTORS BE LIABLE FOR ANY DIRECT, INDIRECT, INCIDENTAL,
%  SPECIAL, EXEMPLARY, OR CONSEQUENTIAL DAMAGES (INCLUDING, BUT NOT
%  LIMITED TO, PROCUREMENT OF SUBSTITUTE GOODS OR SERVICES; LOSS OF USE,
%  DATA, OR PROFITS; OR BUSINESS INTERRUPTION) HOWEVER CAUSED AND ON ANY
%  THEORY OF LIABILITY, WHETHER IN CONTRACT, STRICT LIABILITY, OR TORT
%  (INCLUDING NEGLIGENCE OR OTHERWISE) ARISING IN ANY WAY OUT OF THE USE
